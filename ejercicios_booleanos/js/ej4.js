let number = prompt("Digite un número");
if (number < 10) {
    console.log("El número es menor a 10");
} else if (number == 10) {
    console.log("El número es igual a 10");
} else if (number > 10) {
    console.log("El número es mayor a 10");
} else {
    console.log("El valor dado es incorrecto");
}